import UIKit

class Colors: UIColor {
    class func primaryColor() -> UIColor
    {
        return UIColor(red: 0/255.0, green: 206/255.0, blue: 127/255.0, alpha:1.0)
    }
    
    class func SecondaryColor() -> UIColor
    {
        return UIColor(red: 205/255.0, green: 220/255.0, blue: 57/255.0, alpha:1.0)
    }
    
    class func darkPrimaryColor() -> UIColor
    {
        return UIColor(red: 0/255.0, green: 175/255.0, blue: 108/255.0, alpha:1.0)
    }
}
